package com.slms.webapp.form;

public class DashboardVo {
	
	private int totalOrganisations;
	private int totalCourses;
	private int totalTeachers;
	private int totalStudents;
	
	/**
	 * @return the totalOrganisations
	 */
	public int getTotalOrganisations() {
		return totalOrganisations;
	}
	/**
	 * @param totalOrganisations the totalOrganisations to set
	 */
	public void setTotalOrganisations(int totalOrganisations) {
		this.totalOrganisations = totalOrganisations;
	}
	/**
	 * @return the totalCourses
	 */
	public int getTotalCourses() {
		return totalCourses;
	}
	/**
	 * @param totalCourses the totalCourses to set
	 */
	public void setTotalCourses(int totalCourses) {
		this.totalCourses = totalCourses;
	}
	/**
	 * @return the totalTeachers
	 */
	public int getTotalTeachers() {
		return totalTeachers;
	}
	/**
	 * @param totalTeachers the totalTeachers to set
	 */
	public void setTotalTeachers(int totalTeachers) {
		this.totalTeachers = totalTeachers;
	}
	/**
	 * @return the totalStudents
	 */
	public int getTotalStudents() {
		return totalStudents;
	}
	/**
	 * @param totalStudents the totalStudents to set
	 */
	public void setTotalStudents(int totalStudents) {
		this.totalStudents = totalStudents;
	}

}
